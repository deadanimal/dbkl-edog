<h2><a name="apartment"></a>KEEPING PET DOGS IN MULTI LEVEL BUILDING/HIGH RISE BUILDINGS (SUCH AS CONDOMINIUMS AND APARTMENTS)</h2>
 
<p style="font-size: 14px;">Under the Undang-undang Kecil Perlesenan Anjing Dan Rumah Pembiakan Anjing ( Federal Territory Kuala Lumpur) (Pindaan) 2011 UUK8A “Small dogs in a flat” The Mayor may allow one small dog to be cared for, maintained and protected in a flat.</p>

<p style="font-size: 14px;">Under UUK 2 (b)- small dogs are referred to any breed listed in Schedule II:</p>
<ul>
<li>Minature Pinscher</li>
<li>Bichon Frise</li>
<li>Pekingese</li>
<li>Papilon</li>
<li>Poodle (Toy)</li>
<li>Japanese Chin</li>
<li>Maltese</li>
<li>Pomeranian</li>
<li>Chihuahua</li>
</ul>
<p style="font-size: 14px;">Under UUK 3(1)- Nobody is permitted to care for, keep, maintain or protect any dogs unless the dog has a license according to the ‘Undang-undang kecil’</p>

<p style="font-size: 14px;">Under UUK 3(2)- Only one application for one license:</p>

i) Must got through the mayor in any shape or form which is to be decided by the mayor.
ii) Must be followed by a fee of RM10

<p style="font-size: 14px;">The application for the dog license must be followed by a letter of agreement from the Joint Body Management (JMB) or Management Corporation (MC) when applying.</p>

<p style="font-size: 14px;">Dogs that are care for must not act out/ cause disturbance or hassle to any nearby neighbors or other dog owners.</p>

<p style="font-size: 14px;">The disturbances are as stated:</p>
<ul>
<li>The dogs cause damage to public properties or injury to any civilian or the residents of the building.</li>
<li>The dogs create a noisy environment (day and night) and disturbs the residents.</li>
<li>The dogs are roaming around without supervision.</li>
<li>The dogs are acting out in an aggressive manner that may cause danger/ is dangerous to others.</li>
<li>The dogs owned must be kept at its owner’s home and vicinity only.</li>
</ul>
<p style="font-size: 14px;">The dog owners may only take the dogs out of the building using elevators and the dogs must be constantly supervised.</p>

<p style="font-size: 14px;">The dogs must not be allowed to urinate or defecate  in public areas and if so, the owners must take responsibility to dispose of the dog droppings.</p>

