<h2><a name="terlarang"></a>BAN TOWARDS PROHIBITED DOGS</h2>

<h3>UNDANG-UNDANG KECIL PELESENAN ANJING DAN RUMAH  PEMBIAKAN ANJING ( WPKL ) 1991 ( PINDAAN ) 2011</h3>

<p style="font-size: 14px;">Under UUK 8B (1) , nobody is permitted to keep, maintain, care or protect any breed of dogs that are prohibited.
</p>
<p style="font-size: 14px;">Undang-undang kecil 2 (d) – prohibited breed of a dog is such as those stated under schedule III </p>

<ul>
<li>Pit Bull Terrier / Pit Bull ( Also known as American Pit Bull , American Pit Bull , American Pit Bull Terrier, American Staffordshire Terrier and Stafford Bull Terrier )</li>
<li>American Bulldog</li>
<li>Neapolitan Mastiff</li>
<li>Japanese Tosa</li>
<li>Akita</li>
<li>Dogo Argentino</li>
<li>Fila Braziliero</li>
</ul>


<p style="font-size: 14px;">Under UUK 8B (2) , the Mayor must seize and dispose of whichever prohibited breed of dog that is found at any premise or anywhere in public.</p>

<p style="font-size: 14px;">Under UUK 8B (3), there would be no reparation that is to be payed by the mayor for the disposal of these dogs.</p>

