                                <h2><a name="tanggungjawab"></a>RESPONSIBILITY OF CONTROLLED DOG LICENSE</h2>

<h3>UNDANG-UNDANG KECIL PELESENAN ANJING DAN RUMAH  PEMBIAKAN ANJING ( WPKL ) 1991 ( PINDAAN ) 2011</h3>

<p style="font-size: 14px;">Under UUK 3(2) – one application for one license: (c) in the manner of controlled dogs, must be done after-</p>

<ul>
<li>The applicant has followed and passed the course for the handling of dogs and</li>
<li>The dogs have followed and passed the course “Canine Good Citizen” which is launched by any organization authorized by the Mayor</li>
</ul>
<p style="font-size: 14px;">Under UUK 8C, an individual who was granted a license to keep, breed, own and protect a controlled dog must:</p>
<ul>
<li>The dogs owned must be under constant supervision at all times.</li>
<li>The dogs must always be on a leash and as its mouth covered whenever it leaves the premise.</li>
<li>Ensure the dogs does not leave its area designated area.</li>
<li>Ensure the premise is gated and safe and and that the gate is well maintained at all times.</li>
<li>Ensure a sign displaying “BEWARE OF DOG” is placed and that it is easily visible to the public.</li>
</ul>
<p style="font-size: 14px;">Under UUK 2 ( c )-a supervised dog refers to any dog listed under schedule IV</p>
<ul>
<li>Rottweiller</li>
<li>Doberman</li>
<li>German Shepard / Alsatian including Belgian Shepard dan East European Shepard</li>
<li>Bull Mastiff</li>
<li>Bull Terrier</li>
<li>Perro de Presa Canario ( also known as Canary Dog )</li>
</ul>
