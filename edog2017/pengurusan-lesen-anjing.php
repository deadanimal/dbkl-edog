<h2><a name="apartment"></a>PEMELIHARAAN ANJING DI BANGUNAN KEDIAMAN BERTINGKAT ( SEPERTI DI KONDO ATAU APARTMENT )</h2>
 
<p style="font-size: 14px;">Dibawah Undang-Undang Kecil Pelesenan Anjing Dan Rumah Pembiakan Anjing ( Wilayah Persekutuan Kuala Lumpur ) ( Pindaan ) 2011 UUK 8A ” Anjing kecil di flat” Datuk Bandar boleh membenarkan seekor anjing kecil dipelihara, dilindungi atau  disenggara di suatu flat.</p>

<p style="font-size: 14px;">Dibawah UUK 2 ( b ) - anjing kecil ertinya mana-mana anjing sebagaimana yang dinyatakan dalam JADUAL II :</p>
<ul>
<li>Minature Pinscher</li>
<li>Bichon Frise</li>
<li>Pekingese</li>
<li>Papilon</li>
<li>Poodle (Toy)</li>
<li>Japanese Chin</li>
<li>Maltese</li>
<li>Pomeranian</li>
<li>Chihuahua</li>
</ul>
<p style="font-size: 14px;">Dibawah UUK 3 ( 1 ) – Tiada seorang pun boleh mempunyai, memelihara, melindungi atau menyenggara mana-mana anjing melainkan  jika anjing itu dilesenkan menurut Undang-Undang Kecil ini.</p>

<p style="font-size: 14px;">Dibawah UUK 3 ( 2 ) – sesuatu permohonan bagi suatu lesen :</p>

i) hendaklah dibuat kepada Datuk Bandar mengikut apa-apa bentuk sebagaimana yang ditentukan oleh Datuk Bandar.
ii) hendaklah disertakan dengan fi sebanyak sepuluh ringgit

<p style="font-size: 14px;">Permohonan untuk lesen anjing kecil hendaklah disertakan bersama surat persetujuan daripada Joint Management Body ( JMC ) atau Management Corporation ( MC ) semasa memohon lesen.</p>

<p style="font-size: 14px;">Anjing yang dipelihara tidak boleh menimbulkan suatu masalah / kacau ganggu atau mewujudkan sebarang gangguan yang tidak munasabah kepada penghuni lain.</p>

<p style="font-size: 14px;">Contoh kacau-ganggu adalah seperti :-</p>
<ul>
<li>Anjing yang dipelihara melakukan / menyebabkan kerosakan harta benda awam atau kecerderaan kepada penduduk di bangunan kediaman tersebut.</li>
<li>Anjing yang dipelihara membuat bising secara berterusan (siang atau malam)  dan menimbulkan masalah kacau ganggu bunyi bising kepada penduduk setempat . </li>
<li>Anjing yang dipelihara dibiarkan berkeliaran bebas tanpa pengawasan.</li>
<li>Anjing yang dipelihara mempamerkan tingkah laku yang agresif atau berbahaya atau mungkin merbahaya.</li>
<li>Anjing yang dipelihara mesti terhad kepada unit pemilik dan tidak dibenarkan berkeliaran bebas.</li>
</ul>
<p style="font-size: 14px;">Pemilik boleh mengambil anjing yang dipelihara keluar dari bangunan tersebut hanya melalui perkhidmatan lif dan anjing yang dipelihara perlu berada dibawah pengawasan / kawalan pemilik setiap masa.</p>

<p style="font-size: 14px;">Anjing yang dipelihara tidak dibenarkan untuk kencing atau membuang air besar di kawasan umum. Sekiranya ini berlaku, pemilik perlu bertanggungjawab untuk segera membersihkan selepas haiwan peliharaannya melakukan kesalahan tersebut.</p>

