                                <h2><a name="tanggungjawab"></a>TANGGUNGJAWAB PEMUNYA BERLESEN ANJING TERKAWAL</h2>

<h3>UNDANG-UNDANG KECIL PELESENAN ANJING DAN RUMAH  PEMBIAKAN ANJING ( WPKL ) 1991 ( PINDAAN ) 2011</h3>

<p style="font-size: 14px;">Di bawah UUK 3 (2) - suatu permohonan bagi suatu lesen: (c) dalam hal anjing terkawal, hendakah dibuat selepas:</p>

<ul>
<li>pemohon telah mengikuti dan lulus kursus pengendalian anjing dan</li>
<li>anjing itu telah mengikuti dan lulus kursus "Canine Good Citizen" yang dianjurkan oleh mana-mana badan yang ddiluluskan oleh Datuk Bandar</li>
</ul>
<p style="font-size: 14px;">Di bawah UUK 8C, seseorang yang diberikan lesen untuk mempunyai,  memelihara, melindungi atau menyenggara anjing terkawal hendaklah:</p>
<ul>
<li>Memelihara anjing itu di bawah  pengawasan yang sepatutnya pada setiap masa.</li>
<li>Memastikan bahawa anjing itu diikat dan ditutup mulutnya dan berada di bawah kawalan yang sepatutnya pada setiap masa bila mana ia dbawa keluar dari premis.</li>
<li>Memastikan bahawa anjing itu tidak berkeliaran keluar dari tempat ia dipelihara.</li>
<li>Memastikan bahawa premisnya dipagar dengan selamat dan pagar itu disenggarakan dengan sepatutnya pada setiap masa.</li>
<li>Mempamerkan pada setiap masa tanda ” AWAS ANJING ” di suatu tempat yang ketara dan mudah dilihat di bahagian hadapan dan belakang premisnya.</li>
</ul>
<p style="font-size: 14px;">Dibawah UUK 2 ( c ) – anjing terkawal ertinya mana-mana anjing sebagaimana yang dinyatakan dalam JADUAL IV</p>
<ul>
<li>Rottweiller</li>
<li>Doberman</li>
<li>German Shepard / Alsatian termasuk Belgian Shepard dan East European Shepard</li>
<li>Bull Mastiff</li>
<li>Bull Terrier</li>
<li>Perro de Presa Canario ( juga dikenali sebagai Canary Dog )</li>
</ul>
