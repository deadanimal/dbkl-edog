<h2><a name="terlarang"></a>LARANGAN TERHADAP ANJING TERLARANG</h2>

<h3>UNDANG-UNDANG KECIL PELESENAN ANJING DAN RUMAH  PEMBIAKAN ANJING ( WPKL ) 1991 ( PINDAAN ) 2011</h3>

<p style="font-size: 14px;">Dibawah UUK 8B ( 1 ), tiada seorang pun boleh mempunyai, memelihara, melindungi atau menyenggara mana-mana anjing terlarang.
</p>
<p style="font-size: 14px;">Undang – Undang kecil 2  ( d ) – anjing terlarang ertinya mana-mana anjing sebagaimana yang dinyatakan dalam JADUAL III </p>

<ul>
<li>Pit Bull Terrier / Pit Bull ( juga dikenali sebagai American Pit Bull, American Pit Bull Terrier, American Staffordshire Terrier dan Stafford Bull Terrier )</li>
<li>American Bulldog</li>
<li>Neapolitan Mastiff</li>
<li>Japanese Tosa</li>
<li>Akita</li>
<li>Dogo Argentino</li>
<li>Fila Braziliero</li>
</ul>


<p style="font-size: 14px;">Dibawah UUK 8B ( 2 ), Datuk Bandar hendaklah menyita dan melupuskan mana-mana anjing terlarang yang dijumpai dimana-mana premis atau dijumpai berkeliaran di tempat awam.</p>

<p style="font-size: 14px;">Dibawah UUK 8B ( 3 ), tiada pampasan boleh kena dibayar oleh Datuk Bandar bagi pelupusan anjing itu.</p>

